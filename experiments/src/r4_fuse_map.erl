%%====================================================================
%% to_name
%%====================================================================

to_name({{r4, XX, YY}, {mux, MM}, Value}, Density) ->
    case to_name(XX, YY, MM, Density) of
        {x, _} ->
            false;

        {_, x} ->
            false;

        {X, I} ->
            {ok, {{r4, X, YY}, {interconnect, I}, Value}}
    end;
to_name({{r4, XX, YY}, {mux, MM}, Key, Value}, Density) ->
    case to_name(XX, YY, MM, Density) of
        {x, _} ->
            false;

        {_, x} ->
            false;

        {X, I} ->
            {ok, {{r4, X, YY}, {interconnect, I}, Key, Value}}
    end;
to_name(_, _) ->
    false.

%%--------------------------------------------------------------------

